# Урок 3: Условный оператор if/elif/else

print("🤖 Привет! Я — простой ИИ-ассистент.")
print("Я умею отвечать на несколько команд.")
print("Доступные команды: привет, погода, выход")
print("-" * 40)

while True:
    command = input("\nВведи команду: ").lower().strip()

    if command == "привет":
        print("👋 Привет, dsquared! Рад тебя видеть.")
    elif command == "погода":
        print("☀️ Сегодня отличная погода для кодинга!")
    elif command == "выход":
        print("👋 До встречи!")
        break
    else:
        print("❓ Неизвестная команда. Попробуй: привет, погода, выход")
